package problem096

import "fmt"

type position struct {
	x, y int
}

type sudoku struct {
	fields map[position]int
}

func newSudoku(vals [][]int) *sudoku {
	fields := make(map[position]int)
	for k1, row := range vals {
		for k2, v := range row {
			fields[position{k1, k2}] = v
		}
	}

	return &sudoku{fields: fields}
}

// bruteforce
func (s *sudoku) solve() bool {
	// find empty field
	p, exists := s.firstEmptyPosition()
	if !exists {
		return true
	}

	// try to assign a number to position
	for i := 1; i <= 9; i++ {
		if s.canAttach(p, i) {
			// attach
			s.fields[p] = i

			// check if sudoku is solved already
			if s.solve() {
				return true
			}

			// something went wrong, assign value back to zero and try something else
			s.fields[p] = 0
		}
	}

	return false
}

// If empty positions exist, sudoku is not finished
func (s *sudoku) isFinished() bool {
	_, emptyFieldExists := s.firstEmptyPosition()
	return !emptyFieldExists
}

// iterate in order.
func (s *sudoku) firstEmptyPosition() (position, bool) {
	for row := 0; row < 9; row++ {
		for col := 0; col < 9; col++ {
			if s.fields[position{row, col}] == 0 {
				return position{row, col}, true
			}
		}
	}
	return position{-1, -1}, false
}

// check if value can be attached to position
func (s *sudoku) canAttach(p position, value int) bool {
	if s.usedInRow(p.x, value) {
		return false
	}

	if s.usedInColumn(p.y, value) {
		return false
	}

	// we need to define box borders here
	if s.usedInBox(p.x-p.x%3, p.y-p.y%3, value) {
		return false
	}

	return true
}

func (s *sudoku) usedInRow(row, value int) bool {
	for y := 0; y < 9; y++ {
		if s.fields[position{row, y}] == value {
			return true
		}
	}

	return false
}

func (s *sudoku) usedInColumn(col, value int) bool {
	for x := 0; x < 9; x++ {
		if s.fields[position{x, col}] == value {
			return true
		}
	}

	return false
}

func (s *sudoku) usedInBox(startRow, startCol, value int) bool {
	for i := 0; i < 3; i++ {
		for j := 0; j < +3; j++ {
			if s.fields[position{i + startRow, j + startCol}] == value {
				return true
			}
		}
	}

	return false
}

func (s *sudoku) print() {
	for row := 0; row < 9; row++ {
		if row%3 == 0 {
			fmt.Println()
		}

		for col := 0; col < 9; col++ {
			if col%3 == 0 {
				fmt.Print("   ")
			}

			fmt.Printf("%d ", s.fields[position{row, col}])
		}
		fmt.Println()
	}
	fmt.Println("----------------------")

}

func solveSudokus() int {
	sum := 0
	ss := sudokus()
	for _, s := range ss {
		s.solve()
		sum += s.fields[position{0, 0}]*100 + s.fields[position{0, 1}]*10 + s.fields[position{0, 2}]
	}

	return sum
}

